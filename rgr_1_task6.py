import pandas as pd
import numpy as np
from scipy import stats

# ============================================
# ЗАГРУЗКА ДАННЫХ
# ============================================
# Читаем данные из CSV файла в pandas DataFrame
df = pd.read_csv('RGR1_A-7_X1-X4.csv')

# Извлекаем значения столбцов X1, X2, X3 в виде numpy массивов
# .values преобразует pandas Series в массив numpy для удобства вычислений
X1 = df['X1'].values
X2 = df['X2'].values
X3 = df['X3'].values

# Определяем параметры для построения доверительных интервалов
n = len(X1)  # объем выборки (количество наблюдений)
alpha = 0.05  # уровень значимости (вероятность ошибки первого рода)
confidence = 1 - alpha  # доверительная вероятность (95%)

# Выводим основные параметры
print(f"Объем выборки n = {n}")
print(f"Уровень доверия = {confidence*100}%\n")

# ============================================
# 4.6.1. АСИМПТОТИЧЕСКИЙ ДОВЕРИТЕЛЬНЫЙ ИНТЕРВАЛ ДЛЯ МАТЕМАТИЧЕСКОГО ОЖИДАНИЯ EX
# ============================================
# Асимптотический интервал использует нормальное распределение (Центральная предельная теорема)
# Он приближенно верен для больших выборок, даже если данные не нормально распределены

# Критическое значение стандартного нормального распределения для заданного уровня значимости
# stats.norm.ppf - это обратная функция распределения (квантиль)
# Для 95% доверительного интервала: z_{0.975} ≈ 1.96
z_crit = stats.norm.ppf(1 - alpha/2)

def asymptotic_ci(data):
    """
    Функция для построения асимптотического доверительного интервала для математического ожидания
    Основана на Центральной предельной теореме (работает при больших выборках)
    
    Параметры:
    data - массив выборочных данных
    
    Возвращает:
    mean - выборочное среднее
    std - выборочное стандартное отклонение (исправленное)
    lower - нижняя граница доверительного интервала
    upper - верхняя граница доверительного интервала
    """
    mean = np.mean(data)  # выборочное среднее (точечная оценка)
    std = np.std(data, ddof=1)  # исправленное стандартное отклонение (ddof=1 - деление на n-1)
    # Предельная ошибка: z * (σ̂ / √n)
    margin = z_crit * std / np.sqrt(n)
    # Доверительный интервал: (x̄ - погрешность; x̄ + погрешность)
    return mean, std, mean - margin, mean + margin

print("Асимптотические доверительные интервалы для математического ожидания EX:")
print(f"{'':<5} {'Среднее x̄':<10} {'Станд. откл. σ̂':<10} {'Нижняя граница':<12} {'Верхняя граница':<12} {'Ширина':<10}")
print("-"*70)

# Вычисляем и выводим доверительные интервалы для X1, X2, X3
for name, data in [("X1", X1), ("X2", X2), ("X3", X3)]:
    mean, std, lower, upper = asymptotic_ci(data)
    print(f"{name:<5} {mean:<10.4f} {std:<10.4f} {lower:<12.4f} {upper:<12.4f} {upper-lower:<10.4f}")

# Сохраняем асимптотический интервал для X3 для последующего сравнения
ci_X3_asymp = asymptotic_ci(X3)[2:]  # берем только нижнюю и верхнюю границы

# ============================================
# 4.6.2. ТОЧНЫЕ ДОВЕРИТЕЛЬНЫЕ ИНТЕРВАЛЫ ДЛЯ НОРМАЛЬНОГО РАСПРЕДЕЛЕНИЯ (X3)
# ============================================
# Точные интервалы требуют предположения о нормальном распределении данных
# Они дают более точные результаты, особенно для малых выборок
print("\n" + "="*70)
print("Точные доверительные интервалы для X3 (предполагаем нормальное распределение):")
print("="*70)

# Вычисляем основные статистики для X3
mean_X3 = np.mean(X3)  # выборочное среднее
std_X3 = np.std(X3, ddof=1)  # исправленное стандартное отклонение
var_X3 = std_X3 ** 2  # выборочная дисперсия

print(f"\nВыборочные характеристики X3:")
print(f"  Среднее x̄ = {mean_X3:.4f}")
print(f"  Стандартное отклонение σ̂ = {std_X3:.4f}")
print(f"  Дисперсия σ̂² = {var_X3:.4f}")

# 1. Точный доверительный интервал для математического ожидания μ
# Используем распределение Стьюдента (t-распределение), т.к. дисперсия неизвестна
# Для нормальных данных статистика (x̄ - μ) / (σ̂/√n) имеет t-распределение с n-1 степенями свободы
t_crit = stats.t.ppf(1 - alpha/2, df=n-1)  # критическое значение t-распределения
margin_t = t_crit * std_X3 / np.sqrt(n)  # предельная ошибка
ci_mu_lower = mean_X3 - margin_t  # нижняя граница
ci_mu_upper = mean_X3 + margin_t  # верхняя граница

print(f"\n1) Точный доверительный интервал для математического ожидания μ:")
print(f"   Критическое значение t_{1-alpha/2, {n-1}} = {t_crit:.4f}")
print(f"   Доверительный интервал для μ: ({ci_mu_lower:.4f}; {ci_mu_upper:.4f})")
print(f"   Ширина интервала: {ci_mu_upper - ci_mu_lower:.4f}")
print(f"   Асимптотический интервал: ({ci_X3_asymp[0]:.4f}; {ci_X3_asymp[1]:.4f})")
print(f"   Разница в ширине: {(ci_mu_upper - ci_mu_lower) - (ci_X3_asymp[1] - ci_X3_asymp[0]):.4f}")

# 2. Точный доверительный интервал для дисперсии σ²
# Используем распределение хи-квадрат (χ²), т.к. для нормальных данных
# статистика (n-1) * S² / σ² имеет χ²-распределение с n-1 степенями свободы
chi2_lower = stats.chi2.ppf(alpha/2, df=n-1)  # нижний квантиль χ²
chi2_upper = stats.chi2.ppf(1 - alpha/2, df=n-1)  # верхний квантиль χ²

# Доверительный интервал для дисперсии: 
# ((n-1)S²/χ²_{верхн}, (n-1)S²/χ²_{нижн})
ci_var_lower = (n-1) * var_X3 / chi2_upper
ci_var_upper = (n-1) * var_X3 / chi2_lower

print(f"\n2) Точный доверительный интервал для дисперсии σ²:")
print(f"   Нижний квантиль χ²_{alpha/2, {n-1}} = {chi2_lower:.4f}")
print(f"   Верхний квантиль χ²_{1-alpha/2, {n-1}} = {chi2_upper:.4f}")
print(f"   Доверительный интервал для σ²: ({ci_var_lower:.4f}; {ci_var_upper:.4f})")
print(f"   Ширина интервала: {ci_var_upper - ci_var_lower:.4f}")
print(f"   Точечная оценка дисперсии σ̂² = {var_X3:.4f}")

# 3. Доверительный интервал для стандартного отклонения σ
# Получаем путем извлечения квадратного корня из границ интервала для дисперсии
ci_sigma_lower = np.sqrt(ci_var_lower)
ci_sigma_upper = np.sqrt(ci_var_upper)

print(f"\n3) Доверительный интервал для стандартного отклонения σ:")
print(f"   Доверительный интервал для σ: ({ci_sigma_lower:.4f}; {ci_sigma_upper:.4f})")
print(f"   Ширина интервала: {ci_sigma_upper - ci_sigma_lower:.4f}")
print(f"   Точечная оценка стандартного отклонения σ̂ = {std_X3:.4f}")

# ============================================
# 4.6.3. СРАВНЕНИЕ С ДРУГИМИ СТОЛБЦАМИ
# ============================================
# Сравниваем точный интервал (для нормального распределения) с асимптотическими
print("\n" + "="*70)
print("Сравнение точных и асимптотических доверительных интервалов:")
print("="*70)

print(f"\n{'Столбец':<6} {'Тип интервала':<20} {'Доверительный интервал для μ':<30} {'Ширина':<10}")
print("-"*70)

# X3 - точный интервал (на основе t-распределения)
print(f"{'X3':<6} {'Точный (t-распр.)':<20} ({ci_mu_lower:.4f}; {ci_mu_upper:.4f}) {ci_mu_upper - ci_mu_lower:<10.4f}")

# X3 - асимптотический интервал (на основе нормального распределения)
print(f"{'X3':<6} {'Асимптотический':<20} ({ci_X3_asymp[0]:.4f}; {ci_X3_asymp[1]:.4f}) {ci_X3_asymp[1] - ci_X3_asymp[0]:<10.4f}")

# X1 - асимптотический интервал
mean_X1, std_X1, ci_X1_lower, ci_X1_upper = asymptotic_ci(X1)
print(f"{'X1':<6} {'Асимптотический':<20} ({ci_X1_lower:.4f}; {ci_X1_upper:.4f}) {ci_X1_upper - ci_X1_lower:<10.4f}")

# X2 - асимптотический интервал
mean_X2, std_X2, ci_X2_lower, ci_X2_upper = asymptotic_ci(X2)
print(f"{'X2':<6} {'Асимптотический':<20} ({ci_X2_lower:.4f}; {ci_X2_upper:.4f}) {ci_X2_upper - ci_X2_lower:<10.4f}")